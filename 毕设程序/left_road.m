function zukang_left = left_road(n,flag)
global e_1;
x_i1 = e_1 /100 / 72;
if flag == 1
    zukang_left = 20 * (1 + 0.15 * (n / e_1) ^ 0.4)...
        + (10 * (1 - 0.5 ) ^ 2) / (2 * (1 - 0.5 * x_i1))...
        + (x_i1 ^ 2) / (2 * n * (1 - x_i1))...
        -  0.65 * ((10 / (n ^ 2)) ^ (1/3)) * (x_i1 ^ (2 + 5 * x_i1));
else
    zukang_left = (10 * (1 - 0.5 ) ^ 2) / (2 * (1 - 0.5 * x_i1))...
        + (x_i1 ^ 2) / (2 * n * (1 - x_i1))...
        -  0.65 * ((10 / (n ^ 2)) ^ (1/3)) * (x_i1 ^ (2 + 5 * x_i1));
end
end

