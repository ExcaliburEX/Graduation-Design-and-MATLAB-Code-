function zukang_right = right_road(n,flag)
global e_3;
x_i3 = e_3 /100 / 80;
if flag == 1
    zukang_right = 20 * (1 + 0.15 * (n / e_3) ^ 0.4)...
       + (10 * (1 - 0.5 ) ^ 2) / (2 * (1 - 0.5 * x_i3)) ...
       + (x_i3 ^ 2) / (2 * n * (1 - x_i3))...
       -  0.65 * ((10 / (n ^ 2)) ^ (1/3)) * (x_i3 ^ (2 + 5 * x_i3));
else
    zukang_right = (10 * (1 - 0.5 ) ^ 2) / (2 * (1 - 0.5 * x_i3)) ...
       + (x_i3 ^ 2) / (2 * n * (1 - x_i3))...
       -  0.65 * ((10 / (n ^ 2)) ^ (1/3)) * (x_i3 ^ (2 + 5 * x_i3));
end
end

